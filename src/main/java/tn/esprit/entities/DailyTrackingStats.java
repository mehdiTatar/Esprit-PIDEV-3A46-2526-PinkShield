package tn.esprit.entities;

public class DailyTrackingStats {
    private int entryCount;
    private Double averageMood;
    private Double averageStress;
    private Double averageEnergyLevel;
    private Double averageSleepHours;
    private Double averageWaterIntake;
    private Double averagePhysicalActivityLevel;

    public int getEntryCount() {
        return entryCount;
    }

    public void setEntryCount(int entryCount) {
        this.entryCount = entryCount;
    }

    public Double getAverageMood() {
        return averageMood;
    }

    public void setAverageMood(Double averageMood) {
        this.averageMood = averageMood;
    }

    public Double getAverageStress() {
        return averageStress;
    }

    public void setAverageStress(Double averageStress) {
        this.averageStress = averageStress;
    }

    public Double getAverageEnergyLevel() {
        return averageEnergyLevel;
    }

    public void setAverageEnergyLevel(Double averageEnergyLevel) {
        this.averageEnergyLevel = averageEnergyLevel;
    }

    public Double getAverageSleepHours() {
        return averageSleepHours;
    }

    public void setAverageSleepHours(Double averageSleepHours) {
        this.averageSleepHours = averageSleepHours;
    }

    public Double getAverageWaterIntake() {
        return averageWaterIntake;
    }

    public void setAverageWaterIntake(Double averageWaterIntake) {
        this.averageWaterIntake = averageWaterIntake;
    }

    public Double getAveragePhysicalActivityLevel() {
        return averagePhysicalActivityLevel;
    }

    public void setAveragePhysicalActivityLevel(Double averagePhysicalActivityLevel) {
        this.averagePhysicalActivityLevel = averagePhysicalActivityLevel;
    }
}
